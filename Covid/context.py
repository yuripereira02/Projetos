import streamlit as st


st.header('COVID-19')

st.write('''
    A COVID-19 foi uma pandemia causada pelo coronavírus SARS-CoV-2,
    identificada pela primeira vez em dezembro de 2019, na cidade de Wuhan,
    na China. Em poucos meses, a doença se espalhou rapidamente por todos os continentes,
    levando a Organização Mundial da Saúde (OMS) a declarar, em março de 2020, uma emergência de saúde pública global.
''')

st.subheader('Origem e Disseminação')

st.write('''
        O vírus, inicialmente associado a um mercado de frutos do mar e animais vivos em Wuhan,
        mostrou-se altamente transmissível entre pessoas. Com o aumento das viagens internacionais
        e da mobilidade urbana, a disseminação ocorreu de forma acelerada, atingindo grandes centros populacionais e,
        em seguida, regiões mais isoladas.
''')


st.subheader('Impactos na Sociedade')

st.write('''
    A COVID-19 não afetou apenas a saúde das pessoas, mas também transformou profundamente a economia,
    a educação, as relações sociais e a forma como trabalhamos. Entre os principais efeitos, podemos destacar:

    - Sistemas de saúde sobrecarregados, com hospitais lotados e profissionais na linha de frente.

    - Medidas de isolamento social e lockdowns, que impactaram o comércio, a indústria e a vida cotidiana.

    - Avanço da tecnologia digital, com o crescimento do home office, ensino a distância e telemedicina.

    - Perdas humanas significativas, que marcaram famílias em todo o mundo.
''')

st.subheader('Respostas e Avanços')

st.write('''
    Governos, cientistas e organizações internacionais uniram esforços para conter a pandemia. Entre as medidas, estiveram:
    - Campanhas de prevenção, como o uso de máscaras, higienização frequente das mãos e distanciamento físico.
    
    - Desenvolvimento rápido de vacinas, um marco histórico na ciência, que permitiu reduzir casos graves e óbitos em tempo recorde.

    - Colaboração global, com a troca de informações e tecnologias entre países.
''')

st.subheader('Lições Aprendidas')

st.write('''
        A pandemia de COVID-19 revelou a importância da cooperação internacional,
        da investigação científica e da saúde pública como pilares fundamentais para enfrentar crises globais.
        Também destacou a necessidade de combater a desinformação e fortalecer a confiança em fontes oficiais de informação.
''')